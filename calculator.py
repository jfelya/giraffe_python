num1 = input("Enter a number: ")
num2 = input("Enter another number: ")
# int() converts the string to an integer numbers (only whole numbers)
# float() converts the string to a number that can use decimals
total = float(num1) + float(num2)
print("The result of " + str(num1) + " plus " + str(num2) + " is equal to: " + str(total))
